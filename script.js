let currentTaskIndex = 0;
let currentFactIndex = 0;

function showTask() {
    const container = document.getElementById('game-container');
    container.classList.remove('flip-animation', 'paper-bg');
    void container.offsetWidth; // Reset animation
    container.classList.add('flip-animation');
    
    const task = tasks[currentTaskIndex];
    container.innerHTML = "";

    if (task.speaker) {
        container.innerHTML += `<b style="color:#ffcc00">${task.speaker}: </b>`;
    }
    const p = document.createElement('p');
    p.innerText = task.text;
    container.appendChild(p);

    if (task.isPuzzle) {
        renderPuzzle(container, task);
    } else {
        const btnBox = document.createElement('div');
        btnBox.className = "button-container";
        task.options.forEach(opt => {
            const btn = document.createElement('button');
            btn.className = "game-button";
            btn.innerText = opt.text;
            btn.onclick = () => {
                if (opt.action === "showFacts") { currentFactIndex = 0; renderFacts(); }
                else { currentTaskIndex = opt.next; showTask(); }
            };
            btnBox.appendChild(btn);
        });
        container.appendChild(btnBox);
    }
}

function renderFacts() {
    const container = document.getElementById('game-container');
    container.classList.add('paper-bg');
    const fact = martianFacts[currentFactIndex];
    container.innerHTML = `<h2>${fact.title}</h2>
        <div class="fact-content">
            <div class="fact-text-side"><p>${fact.text}</p></div>
            <div class="fact-image-side"><img src="${fact.img}"></div>
        </div>`;
    const nav = document.createElement('div');
    nav.className = "button-container";
    const prev = document.createElement('button');
    prev.innerText = "← Назад"; prev.className = "game-button";
    prev.disabled = currentFactIndex === 0;
    prev.onclick = () => { currentFactIndex--; renderFacts(); };
    const next = document.createElement('button');
    next.innerText = currentFactIndex < martianFacts.length - 1 ? "Далі →" : "Назад";
    next.className = "game-button";
    next.onclick = () => { 
        if (currentFactIndex < martianFacts.length - 1) { currentFactIndex++; renderFacts(); } 
        else showTask(); 
    };
    nav.append(prev, next);
    container.appendChild(nav);
}

function renderPuzzle(container, task) {
    const div = document.createElement('div');
    div.innerHTML = `<img src="${task.image}" class="puzzle-image"><br>
                     <input type="text" id="ans" style="padding:10px; width:60%;" placeholder="Відповідь...">`;
    const btn = document.createElement('button');
    btn.innerText = "ОК";
    btn.className = "game-button";
    btn.onclick = () => {
        if (document.getElementById('ans').value.toLowerCase().trim() === task.answer) {
            currentTaskIndex = task.next;
            showTask();
        } else alert("Невірно!");
    };
    div.appendChild(btn);
    container.appendChild(div);
}

window.onload = showTask;